const mongoose = require('mongoose');

const Schema = mongoose.Schema;

const productSchema = new Schema({
  title: {
    type: String,
    required: true
  },
  price: {
    type: Number,
    required: true
  },
  description: {
    type: String,
    required: true
  },
  image: {
    type: String,
    required: true
  },
  rating: {
    type: Number,
    default: 0,
  },
  numReviews: {
    type: Number,
    default: 0,
  },
  brand: {
    type: String,
    default: ''
  },
  gender: {
    type: String,
    default: ''
  },
  stock: {
    type: Number,
    default: 0
  },
  status: {
    type: String,
    default: ''
  },
  sold: {
    type: Number,
    default: 0
  },
  discount: {
    type: Number,
    default: 0
  },
  code: {
    type: String,
    default: ''
  },
  size: {
    type: Array,
    default: null,
    of: String
  },
  discount_expired_date: {
    type: Date,
    default: null
  },
  subCategoryId: {
    type: Schema.Types.ObjectId,
    ref: 'subcategory',
    required: true
  },
  isFeatured: {
    type: Boolean,
    default: null
  },
  userId: {
    type: Schema.Types.ObjectId,
    ref: 'User'
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
  updatedAt: {
    type: Date,
    default: Date.now,
  },
});

module.exports = mongoose.model('Product', productSchema);

